        <img class="fluid blocked max-650" src="2013/kick_butt_store_front_opt.jpg" style="border: 1px solid red" />
        <p class="red centered smaller no-bottom-margin">Austin Monthly Magazine</p>
        <p class="centered tight-lines no-top-margin no-bottom-margin"><strong>Kick Butt Coffee Music &amp; Booze,<br />
        Java, Martial Arts and Music:<br />
        the Perfect Blend</strong></p>
        <p class="smaller centered no-top-margin">by Lindsey Mullikin</p>
        <p class="smallerer med-lines">WHILE CHATTING WITH A BANK TELLER about the lack of coffee shops along Airport Boulevard, Thomas Gohring came up with what he thought was a kick-butt idea. Merging his love of martial arts (he also owns Master Gohring's Tai Chi &amp; Kung Fu ) with the community's need for a little pick-me-up, he opened Kick Butt Coffee Music &amp; Booze [February 11, 2008].</p>
        <p class="smallerer med-lines">Red walls, nunchuckus, bamboo and oriental fans illustrate the business ties to martial arts. The shop, which serves coffee, smoothies, pizza, breakfast frittatas, sandwiches, wine, beer and liquor, has round, cozy booths, a giant plasma screen TV and a Wii gaming system. Amenities at the new 2nd location include a giant projection screen, a swimming pool and a Wii.</p>
        <p class="smallerer med-lines">But Gohring wanted to build more of a nighttime business with a full bar as well. He asked his sister Joy, a stand-up comedian, to help start a weekly open mic night, and Kick Butt Comedy was born. The small coffee shop even opened its stage to over a 100 out-of-town musicians, including John Biz, Aim Low Kid and Christopher Denny, during South by Southwest.</p>
        <p class="smallerer med-lines">Gohring built off that music foundation with regular blues fusion dance nights, weekly open mic music, monthly Jazz Jam, and monthly belly dancing and drumming as well as regular hip-hop shows and rock concerts. While martial arts, coffee, comedy and music events may seem like random patchwork, it's all tied together with a single thread, Gohring explains. "It kind of boils down to one question that we ask ourselves every step of the way: Is it kick-butt?"</p>
